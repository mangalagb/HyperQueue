package models;

public class Event
{
	public String message;
	public String topic;
	
	public String getMessage()
	{
		return message;
	}
	
	public void setMessage(String s)
	{
		message = s;
	}
	
	public String getTopic()
	{
		return topic;
	}
	
	public void setTopic(String s)
	{
		topic = s;
	}
	
	
}